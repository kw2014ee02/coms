$(document).ready(function () {
    $('#label_city_from').click(function () {
        $('#city_from_search').focus();
    });
    $('#label_city_to').click(function () {
        $('#city_to_search').focus();
    });
    $('#label_date_start').click(function () {
        $('#start_datepicker').focus();
    });
    $('#label_date_end').click(function () {
        $('#end_datepicker').focus();
    });
});